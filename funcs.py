import os

numbers_1 = 10
numbers_2 = 3

def add_numbers(numbers_1, numbers_2):
    '''Функция сумирования чисел'''
    summ_num = numbers_1 + numbers_2
    return summ_num

def difference_numbers(numbers_1, numbers_2):
    '''Функция, реализующая вычитание чисел'''
    diff_num = numbers_1 - numbers_2
    return diff_num


